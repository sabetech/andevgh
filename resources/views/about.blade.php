            <h1>What You Need To Know</h1>
            <p align="justify">
            Liquefied petroleum gas, also called LPG, GPL, LP Gas, liquid petroleum gas or simply propane, is a flammable mixture of hydrocarbon gases used as a fuel in heating appliances and vehicles. It is increasingly used as an aerosol propellant and a refrigerant, replacing chlorofluorocarbons in an effort to reduce damage to the ozone layer. When specifically used as a vehicle fuel it is often referred to as autogas.
Varieties of LPG bought and sold include mixes that are primarily propane (C3H8), primarily butane (C4H10) and, most commonly, mixes including both propane and butane, depending on the season and global geographic location. A powerful odorant, ethanethiol (EN 589), is added to the chemical formulary so that gas leaks can be detected easily. 
LPG is synthesized by refining petroleum or "wet" natural gas, and is almost entirely derived from fossil fuel sources, being manufactured during the refining of petroleum (crude oil), or extracted from petroleum or natural gas streams as they emerge from the ground. It was first produced in 1910 by Dr. Walter Snelling, and the first commercial products appeared in 1912. It currently provides about 3% of all energy consumed, and burns relatively cleanly with no soot and very few sulfur emissions. As it is a gas, it does not pose ground or water pollution hazards, but it can cause air pollution. LPG has a typical specific calorific value of 46.1 MJ/kg compared with 42.5 MJ/kg for fuel oil and 43.5 MJ/kg for premium grade petrol (gasoline).[4] However, its energy density per volume unit of 26 MJ/l is lower than either that of petrol or fuel oil, as its liquid density is almost half.
As its' boiling point is below room temperature, LPG will evaporate quickly at normal temperatures and pressures and is usually supplied in pressurized steel vessels. They are typically filled to between 80% and 85% of their capacity to allow for thermal expansion of the contained liquid. The ratio between the volumes of the vaporized gas and the liquefied gas varies depending on composition, pressure, and temperature, but is typically around 250:1. The pressure at which LPG becomes liquid, called its vapour pressure, likewise varies depending on composition and temperature; for example, it is approximately 220 kilopascals (32 psi) for pure butane at 20 °C (68 °F), and approximately 2.2 megapascals (320 psi) for pure propane at 55 °C (131 °F). LPG is heavier than air, unlike natural gas, and thus will flow along floors and tend to settle in low spots, such as basements. There are two main dangers from this. The first is a possible explosion if the mixture of LPG and air is right and if there is an ignition source. The second is suffocation due to LPG displacing air, causing a decrease in oxygen concentration. Fortunately, LPG is not toxic, so there is no danger of poisoning. In addition, odorants are mixed with all LPG so that leaks can be detected more easily.

